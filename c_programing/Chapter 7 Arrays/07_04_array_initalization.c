#include <stdio.h>

int main()
{
    int a[] = {34, 43, 54};
    printf("The value of a[0] %d\n", a[0]);
    printf("The value of a[1] %d\n", a[1]);
    printf("The value of a[2] %d\n", a[2]);

    float b[] = {3.4, 34.4, 45.5};
    printf("The value of b[0] %f\n", b[0]);
    printf("The value of b[1] %f\n", b[1]);
    printf("The value of b[2] %f\n", b[2]);
    return 0;
}